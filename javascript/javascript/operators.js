// 1. Assignment operator
let a = 10;
console.log(a); 

// 2. Unary operator
a = 20;
a++; 
console.log(a); 

// 3. Arithmetic operator
a = 20;
let b = 50;
console.log(a + b);

// 4. Relational operator
a = 20;
b = 30;
console.log(a < b); 

// 5. Logical operator
a = 20;
b = 30;
console.log(a && b);
console.log(a || b);

// 6. Bitwise operator
a = 30;
b = 40;
console.log(a & b); 

// 7. Ternary operator
const age = 11;
const ba = age >= 18 ? "Major" : "Minor"; 
console.log(ba);
